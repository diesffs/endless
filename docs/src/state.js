// Save entire state to local storage, including characters and their unlocked status
export function saveStateToLocalStorage() {
  localStorage.setItem("gameState", JSON.stringify(state));
}

// Load state from local storage if available
export function loadStateFromLocalStorage() {
  const savedState = localStorage.getItem("gameState");
  if (savedState) {
    const parsedState = JSON.parse(savedState);
    Object.assign(state, parsedState); // Merge saved state into current state
  }
}

// Initialize characters with load function
export function initializeCharacters() {
  loadStateFromLocalStorage();
  updateCharacterUI();
  calculateTotalDPS();
}

// Call initializeCharacters to set up the game state on page load
